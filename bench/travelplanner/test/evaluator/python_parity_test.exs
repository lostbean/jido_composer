defmodule TravelPlanner.Evaluator.PythonParityTest do
  @moduledoc """
  Compares the Elixir evaluator against a pre-computed Python reference fixture.

  The fixture was generated by running a Python evaluator (matching the canonical
  OSU-NLP-Group/TravelPlanner logic) on all 180 validation plans from
  results/val-phase-a-v5/results.jsonl.

  To regenerate the fixture:
    cd bench/travelplanner
    python3 scripts/generate_python_parity_fixture.py
  """

  use ExUnit.Case, async: true

  @moduletag :python_parity

  alias TravelPlanner.Dataset
  alias TravelPlanner.Evaluator
  alias TravelPlanner.ReferenceInfo

  @fixture_path Path.join([__DIR__, "..", "fixtures", "python_parity_val180.jsonl"])
  @results_path Path.join([__DIR__, "..", "..", "results", "val-phase-a-v5", "results.jsonl"])

  # Constraints where we expect known semantic divergences from Python.
  # These are documented, intentional differences — not bugs.
  @known_divergences MapSet.new([
    # Python's is_valid_information_in_current_city does substring city matching
    # against the plan entry text. Elixir does exact DB name lookup, which is
    # stricter but semantically different for edge cases.
    :is_valid_information_in_current_city
  ])

  # Map Python constraint names to Elixir constraint indices
  @commonsense_order [
    :is_valid_plan_length,
    :is_reasonable_visiting_city,
    :is_valid_transportation,
    :is_valid_information_in_current_city,
    :is_valid_restaurants,
    :is_valid_attractions,
    :is_valid_accommodation,
    :is_not_absent
  ]

  # Maps Python fixture key name -> Elixir constraint atom name
  @hard_order [
    :is_valid_cuisine,
    :is_valid_room_rule,
    :is_valid_room_type,
    # Python uses "is_valid_transportation" for the hard constraint,
    # Elixir uses :is_valid_transportation_mode to distinguish from commonsense
    :is_valid_transportation_mode,
    :is_valid_cost
  ]

  # Python fixture uses these keys for hard constraints
  @hard_py_keys [
    "is_valid_cuisine",
    "is_valid_room_rule",
    "is_valid_room_type",
    "is_valid_transportation",
    "is_valid_cost"
  ]

  setup_all do
    tasks = Dataset.load(:validation)
    tasks_by_idx = Map.new(tasks, &{&1.idx, &1})

    fixture_entries =
      @fixture_path
      |> File.read!()
      |> String.split("\n", trim: true)
      |> Enum.map(&Jason.decode!/1)

    plan_entries =
      @results_path
      |> File.read!()
      |> String.split("\n", trim: true)
      |> Enum.map(&Jason.decode!/1)

    {:ok, tasks: tasks_by_idx, fixture: fixture_entries, plans: plan_entries}
  end

  describe "per-constraint parity with Python reference" do
    test "evaluator matches Python fixture on all constraints", context do
      %{tasks: tasks, fixture: fixture, plans: plans} = context

      results = run_parity_comparison(tasks, fixture, plans)

      # Report overall stats
      total = results.total
      agreements = results.agreements
      disagreements = results.disagreements
      known_divergence_count = results.known_divergences

      agreement_rate = if total > 0, do: agreements / total * 100, else: 0

      IO.puts("\n=== Python Parity Results ===")
      IO.puts("Total constraint checks: #{total}")
      IO.puts("Agreements: #{agreements}")
      IO.puts("Disagreements: #{disagreements} (#{known_divergence_count} in known-divergence constraints)")
      IO.puts("Agreement rate: #{Float.round(agreement_rate, 1)}%")

      if results.unexpected_disagreements != [] do
        IO.puts("\nUnexpected disagreements (first 10):")

        results.unexpected_disagreements
        |> Enum.take(10)
        |> Enum.each(fn {idx, constraint, py, ex} ->
          IO.puts("  idx=#{idx} #{constraint}: python=#{py} elixir=#{ex}")
        end)
      end

      # The threshold: we expect >= 97% agreement excluding known divergences
      unexpected_count = length(results.unexpected_disagreements)
      unexpected_rate = if total > 0, do: unexpected_count / total * 100, else: 0

      assert unexpected_rate < 3.0,
             "Unexpected disagreement rate #{Float.round(unexpected_rate, 1)}% exceeds 3% threshold. " <>
               "#{unexpected_count} unexpected disagreements out of #{total} checks."
    end

    test "cost computation agrees with Python for easy tasks", context do
      %{tasks: tasks, fixture: fixture, plans: plans} = context

      # For easy tasks (single destination, 1 person), cost should match closely
      cost_comparisons =
        Enum.zip(fixture, plans)
        |> Enum.filter(fn {fix, _plan} -> fix["has_plan"] end)
        |> Enum.map(fn {fix, plan_entry} ->
          idx = fix["idx"]
          task = tasks[idx]
          db = ReferenceInfo.parse(task.reference_information)
          plan = plan_entry["plan"]

          report = Evaluator.score_plan_detailed(plan, task, db)
          py_cost = fix["total_cost"]
          ex_cost = report.total_cost

          {idx, task.level, py_cost, ex_cost}
        end)
        |> Enum.filter(fn {_idx, level, _py, _ex} -> level == "easy" end)
        |> Enum.filter(fn {_idx, _level, py, ex} -> py != nil and ex != nil end)

      matches =
        Enum.count(cost_comparisons, fn {_idx, _level, py, ex} ->
          abs(py - ex) < 1.0
        end)

      total_easy = length(cost_comparisons)

      IO.puts("\n=== Cost Parity (easy tasks) ===")
      IO.puts("Matching within $1: #{matches}/#{total_easy}")

      if total_easy > 0 do
        match_rate = matches / total_easy * 100
        assert match_rate > 90.0,
               "Cost match rate #{Float.round(match_rate, 1)}% is below 90% for easy tasks"
      end
    end
  end

  defp run_parity_comparison(tasks, fixture, plans) do
    initial = %{
      total: 0,
      agreements: 0,
      disagreements: 0,
      known_divergences: 0,
      unexpected_disagreements: []
    }

    Enum.zip(fixture, plans)
    |> Enum.reduce(initial, fn {fix, plan_entry}, acc ->
      if not fix["has_plan"] do
        acc
      else
        idx = fix["idx"]
        task = tasks[idx]
        db = ReferenceInfo.parse(task.reference_information)
        plan = plan_entry["plan"]

        report = Evaluator.score_plan_detailed(plan, task, db)

        # Compare commonsense constraints
        cs_py_keys = Enum.map(@commonsense_order, &Atom.to_string/1)
        acc = compare_constraints(acc, idx, fix["commonsense"], report.commonsense, @commonsense_order, cs_py_keys)

        # Compare hard constraints
        acc = compare_constraints(acc, idx, fix["hard"], report.hard, @hard_order, @hard_py_keys)

        acc
      end
    end)
  end

  defp compare_constraints(acc, idx, py_results, ex_results, order, py_keys) do
    Enum.reduce(Enum.zip(order, py_keys), acc, fn {constraint_name, py_key}, acc ->
      py_pass = Map.get(py_results, py_key)

      ex_pass =
        case Enum.find(ex_results, fn {name, _pass} -> name == constraint_name end) do
          {_, :ok} -> true
          {_, {:fail, _}} -> false
          nil -> nil
        end

      if py_pass == nil or ex_pass == nil do
        # Skip if either side couldn't evaluate
        acc
      else
        acc = %{acc | total: acc.total + 1}

        if py_pass == ex_pass do
          %{acc | agreements: acc.agreements + 1}
        else
          acc = %{acc | disagreements: acc.disagreements + 1}

          if constraint_name in @known_divergences do
            %{acc | known_divergences: acc.known_divergences + 1}
          else
            %{acc | unexpected_disagreements: [{idx, constraint_name, py_pass, ex_pass} | acc.unexpected_disagreements]}
          end
        end
      end
    end)
  end
end
